package phone.wobo.music.model;

public class Song {
	private int sid;
	private String name;
	private String  hash;
	private String  m4ahash;
	private String  hash320;
	private String time;
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHash() {
		return hash;
	}
	public void setHash(String hash) {
		this.hash = hash;
	}
	
	public String getM4ahash() {
		return m4ahash;
	}
	public void setM4ahash(String m4ahash) {
		this.m4ahash = m4ahash;
	}
	public String getHash320() {
		return hash320;
	}
	public void setHash320(String hash320) {
		this.hash320 = hash320;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getTime() {
		return time;
	}
	

}
