package phone.wobo.music.model;

public class SingerFavorites {
	private String keySinger;
	private String singerName;
	private String imgUrl;
	private String keySingerClass;
	
	public void setKeySinger(String keySinger) {
		this.keySinger = keySinger;
	}
	
	public String getKeySinger() {
		return keySinger;
	}
	
	public void setSingerName(String singerName) {
		this.singerName = singerName;
	}
	
	public String getSingerName() {
		return singerName;
	}
	
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	
	public String getImgUrl() {
		return imgUrl;
	}
	
	public void setKeySingerClass(String keySingerClass) {
		this.keySingerClass = keySingerClass;
	}
	
	public String getKeySingerClass() {
		return keySingerClass;
	}
}
